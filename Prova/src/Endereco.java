final public class Endereco {

    private int num;

    private String rua;

    public Endereco() {
        this.num = 0;
        this.rua = "";
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getRua() {
        return rua;
    }

    public void setRua(String rua) {
        this.rua = rua;
    }
}
